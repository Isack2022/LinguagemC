//Programa para conta de 0 a 10 utilizando while
#include <stdio.h>
int main (){
    int x = 0;
    while (x <= 10){/*Laço de reptição que ler varias vezes. Ele é mais pesado que o for mais dependendo do programa você tem que usar o while*/
        printf("%d\n",x);
        x++; 
    }
    return 0;
} 